class Album {
    constructor(albumName, artistName, albumYear, albumTags, albumTracks,
        albumLength, coverImage,) {
      this.albumName = albumName;
      this.artistName = artistName;
      this.albumYear = albumYear;
      this.albumTags = albumTags;
      this.albumTracks = albumLength;
      this.coverImage = coverImage;
    }
  }